    public boolean validarDatos() {
        mensajeErrorEditarUsuario.setText("");

        if (!isRegistrado()) {
            mensajeErrorEditarUsuario.setText("La cedula no se encuentra registrada");
            return false;
        }

        //verifica cada datos debe tener por lo menos 2 caracteres y sin espacios en blanco al inicio y final
        if (PrimNom.getText().trim().length() < 2) {
            mensajeErrorEditarUsuario.setText("El primer nombre debe contener mas de 2 letras");
            return false;
        }

        if (PrimAp.getText().trim().length() < 2) {
            mensajeErrorEditarUsuario.setText("El primer apellido debe contener mas de 2 letras");
            return false;
        }

        if (Cedula.getText().trim().length() <= 0) {
            mensajeErrorEditarUsuario.setText("Se requiere una cedula");
            return false;
        }

        if (!Cedula.getText().trim().matches("^([0-9])*$")) {
            mensajeErrorEditarUsuario.setText("Solamente se permite numero en la cedula");
            return false;
        }

        if (Contra.getText().trim().length() == 0) {
            mensajeErrorEditarUsuario.setText("Debe ingresar una contrase√±a");
            return false;
        }

        mensajeErrorEditarUsuario.setText("");
        return true;
    }

    public boolean isRegistrado() {
        File ruta = new File("usuarios.csv");
        FileReader lector;
        try {
            lector = new FileReader(ruta);
            BufferedReader bf = new BufferedReader(lector);
            String linea = bf.readLine();
            String[] datosPorPartes;
            while (linea != null) {
                datosPorPartes = linea.split(";");
                //verifica si se encuentra registrado la cedula
                if (datosPorPartes[4].equals(CedSearch.getText())) {
                    return true;
                }

                linea = bf.readLine();
            }
            lector.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

    public Boolean CedulaExistente(String NewCed) {
        File ruta = new File("usuarios.csv");
        FileReader lector;
        try {
            lector = new FileReader(ruta);
            BufferedReader bf = new BufferedReader(lector);
            String linea = bf.readLine();
            String[] datosPorPartes;
            while (linea != null) {
                datosPorPartes = linea.split(";");
                //verifica si se encuentra registrado la cedula
                if (datosPorPartes[4].equals(NewCed)) {
                    return true;
                }

                linea = bf.readLine();
            }
            lector.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

