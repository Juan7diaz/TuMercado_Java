    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (esEliminarUsuario.isSelected() && isRegistrado()) {
            LecturaEscritura.EliminarUsuario("usuarios.csv", CedSearch.getText());
            this.setVisible(false);
        } else {
            mensajeErrorEditarUsuario.setText("la cedula a eliminar no se encuentra registrada");
        }

        if (!esEliminarUsuario.isSelected()) {
            String segundoNombre = ".";
            String segundoApellido = ".";
            if (SegNom.getText().trim().length() > 0) {
                segundoNombre = SegNom.getText();
            }
            if (SegAp.getText().trim().length() > 0) {
                segundoApellido = SegAp.getText();
            }

            mensajeErrorEditarUsuario.setText("");

            if (validarDatos()) {
                if (!CedulaExistente(Cedula.getText())) {
                    String datos = PrimNom.getText().trim() + ";" + segundoNombre + ";" + PrimAp.getText().trim() + ";" + segundoApellido + ";" + Cedula.getText().trim() + ";" + Contra.getText().trim() + ";" + isAdmin.isSelected();
                    LecturaEscritura.EditarUsuario("usuarios.csv", CedSearch.getText(), datos);
                    this.setVisible(false);
                } else {
                    mensajeErrorEditarUsuario.setText("La cedula que se intenta ingresar ya existe");
                }

            }
            AdminPanel.Access = false;
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

