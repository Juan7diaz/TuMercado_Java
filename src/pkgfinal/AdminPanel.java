    public boolean validacionesFecha2() {
        mensajeErrorFecha2.setText("");

        if (inputFechaInferio2.getText().trim().length() <= 0 && inputFechaSuperior2.getText().trim().length() <= 0) {
            mensajeErrorFecha2.setText("Se requieren 2 fechas");
            return false;
        }

        try {
            SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
            formatoFecha.setLenient(false);
            formatoFecha.parse(inputFechaSuperior2.getText());
            formatoFecha.parse(inputFechaInferio2.getText());
        } catch (ParseException e) {
            mensajeErrorFecha2.setText("la fecha ingresada no es una fecha valida");
            return false;
        }

        mensajeErrorFecha2.setText("");

        return true;
    }

