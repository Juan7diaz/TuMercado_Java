    public boolean validacionesFiltroRango() {
        mensajeErrorFiltroCantidad.setText("");

        if (inputValorMaxSearch.getText().trim().length() <= 0) {
            mensajeErrorFiltroCantidad.setText("Se requiere un valor maximo");
            return false;
        }

        if (!inputValorMaxSearch.getText().trim().matches("^([0-9])*$")) {
            mensajeErrorFiltroCantidad.setText("Solamente se permite numeros");
            return false;
        }

        if (inputValorMinSearch.getText().trim().length() <= 0) {
            mensajeErrorFiltroCantidad.setText("Se requiere un valor minimo");
            return false;
        }

        if (!inputValorMinSearch.getText().trim().matches("^([0-9])*$")) {
            mensajeErrorFiltroCantidad.setText("Solamente se permite numeros");
            return false;
        }

        if (!inputCantSearch.getText().trim().matches("^([0-9])*$")) {
            mensajeErrorFiltroCantidad.setText("Solamente se permite numeros");
            return false;
        }

        mensajeErrorFiltroCantidad.setText("");

        return true;

    }

